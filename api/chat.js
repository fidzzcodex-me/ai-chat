function randomId(len = 7) {
  const chars = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789"
  let s = ""
  for (let i = 0; i < len; i++) s += chars[Math.floor(Math.random() * chars.length)]
  return s
}

function isRejected(raw) {
  return /Generated by BLACKBOX\.AI|not upgraded your account/i.test(raw)
}

function parseAnswer(raw) {
  const think = []
  let body = raw.replace(/<think>([\s\S]*?)<\/think>/g, (_, t) => {
    think.push(t.trim())
    return ""
  })
  const end = body.indexOf("</answer>")
  if (end !== -1) body = body.slice(0, end).trimStart().replace(OPEN_TAG, "")
  return { text: body.trim(), think: think.join("\n\n").trim() || null }
}

async function fetchValidated() {
  const headers = { "user-agent": UA }
  const html = await fetch(`${ORIGIN}/`, { headers }).then(r => r.text()).catch(() => "")
  const chunks = [...new Set([...html.matchAll(/\/_next\/static\/[^"]+\.js/g)].map(m => m[0]))]
  const found = await Promise.all(chunks.map(c =>
    fetch(`${ORIGIN}${c}`, { headers })
      .then(r => r.text())
      .then(js => (js.match(ANCHOR) || [])[1] || null)
      .catch(() => null)
  ))
  return found.find(Boolean) || null
}

function buildPayload(messages, validated, options) {
  const { webSearch = false, agent = "VscodeAgent", maxTokens = 2048 } = options
  return {
    messages,
    id: messages[messages.length - 1].id,
    userSelectedAgent: agent,
    userSelectedModel: null,
    maxTokens,
    validated,
    clickedForceWebSearch: webSearch,
    webSearchModeOption: { autoMode: !webSearch, webMode: webSearch, offlineMode: false },
    codeModelMode: true,
    isPremium: false
  }
}

async function bbRequest(payload) {
  const res = await fetch(`${ORIGIN}/api/chat`, {
    method: "POST",
    headers: {
      "content-type": "application/json",
      "origin": ORIGIN,
      "referer": `${ORIGIN}/chat`,
      "user-agent": UA
    },
    body: JSON.stringify(payload)
  })
  return res.text()
}

async function blackboxChat(prompt, options = {}) {
  const history = Array.isArray(options.history) ? options.history : []
  const userMsg = { id: randomId(), role: "user", content: String(prompt) }
  const wrapped = [{ id: randomId(), role: "system", content: WRAP_SYSTEM }, ...history, userMsg]

  let raw = await bbRequest(buildPayload(wrapped, cachedValidated, options))

  if (isRejected(raw)) {
    const fresh = await fetchValidated()
    if (fresh && fresh !== cachedValidated) {
      cachedValidated = fresh
      raw = await bbRequest(buildPayload(wrapped, cachedValidated, options))
    }
  }

  if (isRejected(raw)) return { ok: false, error: raw.trim() }

  const { text, think } = parseAnswer(raw)
  return {
    ok: true,
    text,
    think,
    history: [...history, userMsg, { id: randomId(), role: "assistant", content: text }]
  }
}

export default async function handler(req, res) {
  res.setHeader("Access-Control-Allow-Origin", "*")
  res.setHeader("Access-Control-Allow-Methods", "POST, OPTIONS")
  res.setHeader("Access-Control-Allow-Headers", "Content-Type")

  if (req.method === "OPTIONS") return res.status(200).end()
  if (req.method !== "POST") return res.status(405).json({ error: "Method not allowed" })

  try {
    const { prompt, history, webSearch, fileContent, fileName } = req.body

    let finalPrompt = prompt
    if (fileContent) {
      finalPrompt = `[File: ${fileName || "uploaded file"}]\n\n${fileContent}\n\n---\n\nUser question: ${prompt}`
    }

    const result = await blackboxChat(finalPrompt, { history, webSearch })
    return res.status(200).json(result)
  } catch (err) {
    return res.status(500).json({ ok: false, error: err.message })
  }
}
